//Write a program that prints the numbers 1, 101 and 1001, each on a separate line.

using System;
namespace PrintNumbers
{
   class PrintName
   {
      static void Main()
      {
         Console.WriteLine("1");
         Console.WriteLine("101");
         Console.WriteLine("1001");

         string a = "1";
         string b = "101";
         string c = "1001";

         Console.WriteLine(a);
         Console.WriteLine(b);
         Console.WriteLine(c);

         int d = 1;
         int e = 101;
         int f = 1001;

         Console.WriteLine(d);
         Console.WriteLine(e);
         Console.WriteLine(f);
      }
   }
}
